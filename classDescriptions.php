                <p> The NRA Range Safety Officer program was developed in
                  response to the demand for a nationally-recognized range
                  safety officer certification. NRA Range Safety Officers are
                  people who possess the knowledge, skills, and attitude
                  essential to organizing, conducting, and supervising safe
                  shooting activities and range operations. NRA Range Safety
                  Officers must be 21 years of age or older. The Range Safety
                  Officer course consists of: Range Safety Officer's roles and
                  responsibilities, range standard operating procedures, range
                  inspection and range rules, firearm stoppages and
                  malfunctions, and Range Safety Briefings which include
                  emergency procedures. Candidates must also receive 90 percent
                  or better on the written exam.</p>
